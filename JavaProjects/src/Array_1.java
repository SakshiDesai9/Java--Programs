import java.lang.*;
public class Array_1
{
    public static void main(String[]args)
    {
        int [] Number={10,20,30,40,50};

        System.out.print("Array length:"+Number.length+"\n");

        for(int i=0;i< Number.length;i++)
        {
            System.out.print("\nElement at index" +i +":"+Number[i]);
        }


    }


}
